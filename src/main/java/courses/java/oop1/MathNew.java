package courses.java.oop1;

public class MathNew {

    public int suma(int x, int y) {
        return x + y;
    }

    public int diferenta(int x, int y) {
        return x - y;
    }

    public boolean isOddNumber(int number) {
        if (number % 2 == 0)
            return false;
        return true;
    }

}
